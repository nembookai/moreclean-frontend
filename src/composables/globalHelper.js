export function correctDate(date, defaultFormat = 'YYYY-MM-DD') {
  let year = null;
  let month = null;
  let day = null;

  // Find the separator
  const separator = date.includes('/') ? '/' :
      (date.includes('-') ? '-' :
          (date.includes('.') ? '.' :
              (date.includes(' ') ? ' ' : null)));

  if (!separator) {
      return null;
  }

  const dateArray = date.split(separator);

  // Find if year contains 4 digits
  dateArray.forEach((value, index) => {
      if (value.length === 4) {
          year = value;
          dateArray.splice(index, 1);
      }
  });

  if (!year) {
      year = dateArray[2];
      dateArray.splice(2, 1);
  }

  dateArray.forEach((value, index) => {
      if (value.length === 3) {
          month = value;
          dateArray.splice(index, 1);
      }
  });

  if (dateArray.length === 1) {
      day = dateArray[0];
  }

  if (!month) {
      if (parseInt(dateArray[0]) > 12) {
          day = dateArray[0];
          month = dateArray[1];
      } else if (parseInt(dateArray[1]) > 12) {
          day = dateArray[1];
          month = dateArray[0];
      }
  }

  if (!month) {
      month = dateArray[0];
      day = dateArray[1];
  }
  if (!year || !month || !day || isNaN(year) || isNaN(month) || isNaN(day)) {
    return null;
  }

  // Convert to Moment.js format
  return moment({ year: parseInt(year), month: parseInt(month) - 1, day: parseInt(day) }).isValid()
      ? moment({ year: parseInt(year), month: parseInt(month) - 1, day: parseInt(day) }).format(defaultFormat)
      : null;
}

