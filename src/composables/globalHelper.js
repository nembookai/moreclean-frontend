export function textColorWhiteOrBlack(color) {
  // Ensure the color is a hex value and remove the leading "#"
  const hexColor = color.replace("#", "");
  
  // Convert the hex to RGB values
  const r = parseInt(hexColor.substring(0, 2), 16);
  const g = parseInt(hexColor.substring(2, 4), 16);
  const b = parseInt(hexColor.substring(4, 6), 16);

  // Calculate the brightness based on the RGB values
  const brightness = (0.299 * r + 0.587 * g + 0.114 * b);

  // Return the appropriate class based on brightness
  return brightness > 145 ? "text-black" : "text-white";
};

